import Color from 'color';

function convertTriangleColors(triangle) {
  return ({
    ...Color(triangle.color).object(),
    points: triangle.points,
  });
}

function convertBacgroundColors(bg) {
  return {
    from: Color(bg.from).object(),
    to: Color(bg.to).object()
  };
}

